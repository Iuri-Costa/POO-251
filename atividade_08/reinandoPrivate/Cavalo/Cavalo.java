package reinandoPrivate.Cavalo;
public class Cavalo {
    private int dentes;
    private String cor;
    private String raca;
    public int getDentes(){
        return dentes;
    }
    public String getCor(){
        return cor;
    }
    public String getRaca(){
        return raca;
    }
    public void setDentes(int dentes){
        this.dentes = dentes;
    }
    public void setCor(String cor){
        this.cor = cor;
    }
    public void setRaca(String raca){
        this.raca = raca;
    }
}
