package reinandoPrivate.Vaca;
public class Vaca {
    private int idade;
    private boolean daLeite;
    private double peso;
    public int getIdade(){
        return idade;
    }
    public boolean getDaLeite(){
        return daLeite;
    }
    public double getPeso(){
        return peso;
    }
    public void setIdade(int idade){
        this.idade = idade;
    }
    public void setDaLeite(boolean daLeite){
        this.daLeite = daLeite;
    }
    public void setPeso(double peso){
        this.peso = peso;
    }
}
